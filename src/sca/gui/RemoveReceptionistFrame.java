    private void loadEmpid()
    {
        try{
        empList= ReceptionistDao.getAllRecpId();
        if(empList.isEmpty())
            {
                JOptionPane.showMessageDialog(null,"No receptionist present!");
                return;
            }
        Iterator<String> it= empList.iterator();
        //jcmRecpId.removeAll();
        while(it.hasNext())
        {
            jcmRecpId.addItem(it.next());
        }
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null,"Problem in Database!","Error!",JOptionPane.ERROR_MESSAGE);
        }
    }
