    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        try
        {
            String eid=jcmRecpId.getSelectedItem().toString();
            boolean result=ReceptionistDao.removeRecp(eid);
            if(result)
            {
                JOptionPane.showMessageDialog(null, "Receptionist Removed Successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                jcmRecpId.removeAllItems();
                loadEmpid();
            }
             else
            {
             JOptionPane.showMessageDialog(null,"Couldn't delete Receptionist!" , "Error!",JOptionPane.ERROR_MESSAGE);
            }  
        }
        catch(SQLException xe)
        {
            JOptionPane.showMessageDialog(null,"Problem in Database!","Error!",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btndeleteActionPerformed

