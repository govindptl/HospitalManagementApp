    private void closeFrame()
    {
       int ans;
        ans = JOptionPane.showConfirmDialog(null,"Are you Sure?","Quitting!",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if(ans == JOptionPane.YES_OPTION){
            DBConnection.closeConnection();
            JOptionPane.showMessageDialog(null,"Thank you for using the Application!!");
            System.exit(0);
        } 
    }
    private boolean validateInputs()
    {
        userid = txtUserId.getText();
        
        password = new String(txtPassword.getPassword());
        if(jrbAdmin.isSelected())
            userType = "Admin";
        else if(jrbRecp.isSelected())
            userType = "Receptionist";
        else if(jrbDoctor.isSelected())
            userType = "Doctor";
        
        if(userid.isEmpty() || password.isEmpty() || userType.isEmpty())
            return false;
        return true;
    }
