    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(validateInputs()==false){
           JOptionPane.showMessageDialog(null,"Please input all values");
           return;
         }
        
        UserPojo u = new UserPojo();
        u.setUserid(userid);
        u.setPassword(password);
        u.setUserType(userType);
        try
        {
            username = UserDao.validateUser(u);
            if(username==null)
            {
             JOptionPane.showMessageDialog(null, "Invalid UserId,Password And UserType!","Error!",JOptionPane.ERROR_MESSAGE);
             return;
            }

           UserProfile.setUsername(username);
           UserProfile.setUserType(userType);
           
           if(jrbAdmin.isSelected())
           {
               AdminPanelFrame adminOptionframe=new AdminPanelFrame();
               adminOptionframe.setVisible(true);
               JOptionPane.showMessageDialog(null, "Logged in as Admin","Success!",JOptionPane.INFORMATION_MESSAGE);  
           }
           else if(jrbRecp.isSelected())
           {
               PatientOptionFrame receptionist=new PatientOptionFrame();
               receptionist.setVisible(true);
               JOptionPane.showMessageDialog(null, "Logged in as Receptionist","Success!",JOptionPane.INFORMATION_MESSAGE);
           }
           else if(jrbDoctor.isSelected())
           {
               DoctorViewFrame doctor=new DoctorViewFrame();
               doctor.setVisible(true);
               JOptionPane.showMessageDialog(null, "Logged in as Doctor","Success!",JOptionPane.INFORMATION_MESSAGE);
           }
           this.dispose();
       }
       catch(SQLException xe)
       {
           JOptionPane.showMessageDialog(null, "Problem in Database!"+xe,"Error!",JOptionPane.ERROR_MESSAGE);
           return;
       }
    }//GEN-LAST:event_jButton1ActionPerformed

